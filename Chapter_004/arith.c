#include <stdio.h>
#include <limits.h>

int main(void)
{
    printf(" 3+2=%2d\n", 3+2);
    printf(" 3-2=%2d\n", 3-2);
    printf(" 3*2=%2d\n", 3*2);
    printf(" 3/2=%2d\n", 3/2);
    printf(" 3%%2=%2d\n", 3%2);

    printf(" 3/ 2=%2d\n", 3/2);
    printf(" 3/-2=%2d\n", 3/-2);
    printf("-3/ 2=%2d\n", -3/2);
    printf("-3/-2=%2d\n", -3/-2);

    printf(" 3%% 2=%2d\n", 3%2);
    printf(" 3%%-2=%2d\n", 3%-2);
    printf("-3%% 2=%2d\n", -3%2);
    printf("-3%%-2=%2d\n", -3%-2);

    printf("1.2 + 3.4 = %g\n", 1.2 + 3.4);
    printf("1.2 - 3.4 = %g\n", 1.2 - 3.4);
    printf("1.2 * 3.4 = %g\n", 1.2 * 3.4);
    printf("1.2 / 3.4 = %g\n", 1.2 / 3.4);

    printf("%.2f\n", 88*0.9);
    printf("%g\n", 88*0.9);
    printf("%d\n", (int)(88*0.9));

    printf("%d\n", (85+90+95+100)/4);
    printf("%f\n", (85+90+95+100)/4.0);
    printf("%.2f\n", (85+90+95+100)/4.0);
    printf("%g\n", (85+90+95+100)/4.0);
    printf("%g\n", (double)((85+90+95+100)/4));

    printf("%x\n", ~0);                      // 0xffffffff
    printf("%x\n", ~0x0f0f00ff);             // 0xf0f0ff00
    printf("%x\n", ~0x01234567);             // 0xfedcba98
    printf("%08x\n", 0x01234567&0x000ff000); // 0x00045000
    printf("%08x\n", 0x01234567&0x0f000f00); // 0x00000000
    printf("%08x\n", 0x01234567|0x0f000f00); // 0x0f234567

    printf("%d\n", 4&3); // 0100 & 0011 = 0000
    printf("%d\n", 5&3); // 0101 & 0011 = 0001
    printf("%d\n", 6&3); // 0110 & 0011 = 0010
    printf("%d\n", 7&3); // 0111 & 0011 = 0011
    printf("%d\n", 10&7); // 1010 & 0111 = 0010
    printf("%d\n", 20&7); // 10100 & 0111 = 0000
    printf("%d\n", 30&7); // 11110 & 0111 = 0110
    printf("%d\n", 40&7); // 101000 & 0111 = 0000

    printf("%08x\n", 0x01234567|0x80000000); // 0x81234567
    printf("%08x\n", 0x01234567^0x0f0f0f0f); // 0x0f3c3c78
    printf("%08x\n", 0x0e2c4a68^0x0f0f0f0f); // 0x010d1b79
    printf("%08x\n", 0x0e2c4a68^0x0e2c4a68); // 0x00000000

    printf("%08x\n", 0x1234<<0); // 0x00001234
    printf("%08x\n", 0x1234<<4); // 0x00012340
    printf("%08x\n", 0x1234<<8); // 0x00123400
    printf("%08x\n", 0x1234<<12); // 0x01234000
    printf("%08x\n", 0x1234<<16); // 0x12340000

    printf("%08x\n",0x1234<<20); // 0x23400000
    printf("%08x\n",0x1234<<24); // 0x34000000
    printf("%08x\n",0x1234<<28); // 0x40000000

    printf("%4d\n", 100);               // 100
    printf("%4d\n", 100<<1);            // 200
    printf("%4d\n", (100<<1)+100);      // 300
    printf("%4d\n", 100<<2);            // 400
    printf("%4d\n", (100<<2)+100);      // 500
    printf("%4d\n", (100<<2)+(100<<1)); // 600
    printf("%4d\n", (100<<3)-100);      // 700
    printf("%4d\n", (100<<3));          // 800
    printf("%4d\n", (100<<3)+100);      // 900
    printf("%4d\n", (100<<3)+(100<<1)); // 1000

    printf("%08x\n", 0x1234>>0); // 0x00001234
    printf("%08x\n", 0x1234>>4); // 0x00000123
    printf("%08x\n", 0x1234>>8); // 0x00000012
    printf("%08x\n", 0x1234>>12); // 0x00000001
    printf("%08x\n", 0x1234>>16); // 0x00000000

    printf("%4d\n", 100);    // 100
    printf("%4d\n", 100>>1); // 50
    printf("%4d\n", 100>>2); // 25
    printf("%4d\n", 100>>3); // 12
    printf("%4d\n", 100>>4); // 6
    printf("%4d\n", 100>>5); // 3
    printf("%4d\n", 100>>6); // 1
    printf("%4d\n", 100>>7); // 0
    printf("%4d\n", 100>>8); // 0

    printf("%d\n", CHAR_BIT); // 8
}
